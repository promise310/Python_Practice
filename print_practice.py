print(f'{name}, Hello! :)')

# integer/ float
print(1 + 2)  # 정수 더하기

print(2 - 3)  # 정수 빼기

print(3 * 4)  # 정수 곱하기

print(4 / 5)  # 정수 나누기

print(5 // 6)  # 정수 나눗셈의 몪

print(6 % 7)  # 정수 나눗셈의 나머지

print(7 ** 8)   # 정수 거듭제곱

# String
